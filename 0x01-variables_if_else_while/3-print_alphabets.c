#include <stdio.h>
/**
 * main - Entry point
 * Return: Always 0 (Success)
 */
int main(void)
{
	char lowerLetter = 'a';
	char upperLetter = 'A';
	while (lowLetter <= 'z')
	{
		putchar(lowLetter);
		lowLetter++;
	}

	while (upperLetter <= 'Z')
	{
		putchar(upperLetter);
		upperLetter++;
	}

	putchar('\n');
	return (0);
}
